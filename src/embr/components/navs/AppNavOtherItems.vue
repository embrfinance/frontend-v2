<template>
  <BalPopover no-pad align="left">
    <template v-slot:activator>
      <div
        class="flex items-center px-4 cursor-pointer relative"
        style="top: -10px"
      >
        <div class="text-4xl">...</div>
      </div>
    </template>
    <div class="w-48">
      <a class="app-nav-other-item" href="https://info.embr.finance"
        >Analytics</a
      >
      <a class="app-nav-other-item" href="https://docs.embr.finance">
        Docs & Help
      </a>
      <a class="app-nav-other-item" href="https://github.com/embrfinanace">
        Github
      </a>
      <a class="app-nav-other-item" href="https://twitter.com/embrfinance">
        Twitter
      </a>
      <a class="app-nav-other-item" href="https://embrfinance.medium.com/">
        Medium
      </a>
      <a class="app-nav-other-item" href="">
        Discord
      </a>
    </div>
  </BalPopover>
</template>

<script lang="ts">
import { computed, defineComponent, PropType } from 'vue';
import useNumbers from '@/composables/useNumbers';
import useBreakpoints from '@/composables/useBreakpoints';
import { Alert } from '@/composables/useAlerts';
import useProtocolDataQuery from '@/embr/composables/queries/useProtocolDataQuery';

export default defineComponent({
  name: 'AppNavOtherItems',

  props: {
    alert: { type: Object as PropType<Alert>, required: true }
  },

  setup() {
    const { fNum } = useNumbers();
    const { upToLargeBreakpoint } = useBreakpoints();

    const protocolDataQuery = useProtocolDataQuery();
    const tvl = computed(
      () => protocolDataQuery.data?.value?.totalLiquidity || 0
    );

    const embrPrice = computed(
      () => protocolDataQuery.data?.value?.embrPrice || 0
    );
    const circulatingSupply = computed(
      () => protocolDataQuery.data.value?.circulatingSupply || 0
    );
    const marketCap = computed(() => {
      return embrPrice.value * circulatingSupply.value;
    });
    const loading = computed(() => protocolDataQuery.isLoading.value);

    return {
      fNum,
      upToLargeBreakpoint,
      embrPrice,
      tvl,
      circulatingSupply,
      marketCap,
      loading
    };
  }
});
</script>

<style scoped>
.app-nav-other-item {
  @apply px-4 py-2 text-gray-300 hover:text-green-500 block hover:bg-gray-700;
}
